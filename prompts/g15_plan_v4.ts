// v4-aware 摘要抽取（L3 Codex + L1 QA 共識修：v2 extractG15Call1Summary 靠 🟢🟡🔵 + Hero blockquote
//   抓摘要，但 v4 改純文字 ●▌▸ → v2 extractor 抓不到 Call2 收斂 → Call3 拿到空 Call2 摘要、跨 call
//   上下文丟失。本 extractor 抓 v4 結構：## 章節標題 + > Hero 5 件套 + ▸/●/▌ 粗體條（L1 速覽與章末收斂））
export function extractG15V4Summary(content: string): string {
  const summary: string[] = []
  for (const raw of content.split('\n')) {
    const t = raw.trim()
    if (!t) continue
    if (/^#{2,3}\s/.test(t)) summary.push(t)            // 章節標題（## / ###）
    else if (/^>\s*\*\*/.test(t)) summary.push(t)       // Hero 5 件套引言框
    else if (/^[▸●▌]\s*\*\*/.test(t)) summary.push(t)   // L1 速覽 / 章末三段式收斂粗體條
  }
  return summary.slice(0, 70).join('\n').slice(0, 3500) // 限 3.5K、避免 Call2/3 input 爆
}

