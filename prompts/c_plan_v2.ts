// 從 Call 1+2 提取摘要給 Call 3
// ═══════════════════════════════════════════════════════════
export function extractCall1And2Summary(call1Content: string, call2Content: string): string {
  const call1Sum = extractCall1Summary(call1Content)

  const summaryParts: string[] = [call1Sum]

  // 從 Call 2 提取 Tier 2 系統的精華
  const tier2Headers = ['四、風水堪輿', '五、姓名學', '六、西洋占星', '七、吠陀占星', '八、易經占卜', '九、人類圖']
  for (const header of tier2Headers) {
    const regex = new RegExp(`##\\s*${header}[\\s\\S]{0,2500}?(?=##\\s*|$)`)
    const match = call2Content.match(regex)
    if (match) {
      summaryParts.push(`【${header.slice(2)}】\n${match[0].slice(0, 800).trim()}`)
    }
  }

  return summaryParts.join('\n\n')
}

