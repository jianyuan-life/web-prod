export function getAgeSegment(birthYear: number): AgeSegment {
  const age = new Date().getFullYear() - birthYear
  if (age <= 10) return 'seg_0_10'
  if (age <= 20) return 'seg_10_20'
  if (age <= 30) return 'seg_20_30'
  if (age <= 40) return 'seg_30_40'
  if (age <= 50) return 'seg_40_50'
  if (age <= 60) return 'seg_50_60'
  return 'seg_60_plus'
}

// 舊版 API 相容（steps.ts 仍呼叫 getAgeGroup）
export function getAgeGroup(birthYear: number): 'toddler' | 'child' | 'teen' | 'adult' | 'elder' {
  const age = new Date().getFullYear() - birthYear
  if (age <= 6) return 'toddler'
  if (age <= 12) return 'child'
  if (age <= 18) return 'teen'
  if (age <= 60) return 'adult'
  return 'elder'
}

