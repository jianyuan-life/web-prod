// 從 Call 1 提取摘要給 Call 2
// ═══════════════════════════════════════════════════════════
export function extractCall1Summary(call1Content: string): string {
  const summaryParts: string[] = []

  // 命格總覽儀表板摘要（取前 1500 字）
  const dashboardMatch = call1Content.match(/##\s*命格總覽儀表板[\s\S]{0,3000}?(?=##\s*一|$)/)
  if (dashboardMatch) summaryParts.push(`【命格總覽】\n${dashboardMatch[0].slice(0, 1500).trim()}`)

  // 八字摘要（取 1.1 完整排盤 + 1.5 格局）
  const baziMatch = call1Content.match(/##\s*一、八字命理[\s\S]{0,5000}?(?=##\s*二|$)/)
  if (baziMatch) {
    const baziHead = baziMatch[0].slice(0, 1500)
    const baziPattern = baziMatch[0].match(/###\s*1\.5[\s\S]{0,500}?(?=###|$)/)?.[0] || ''
    summaryParts.push(`【八字】\n${baziHead}${baziPattern ? '\n...\n' + baziPattern : ''}`)
  }

  // 紫微摘要（取命宮 + 關鍵宮位）
  const ziweiMatch = call1Content.match(/##\s*二、紫微斗數[\s\S]{0,3000}?(?=##\s*三|$)/)
  if (ziweiMatch) summaryParts.push(`【紫微】\n${ziweiMatch[0].slice(0, 1500).trim()}`)

  // 奇門摘要
  const qimenMatch = call1Content.match(/##\s*三、奇門遁甲[\s\S]{0,2000}?(?=##\s*四|$)/)
  if (qimenMatch) summaryParts.push(`【奇門】\n${qimenMatch[0].slice(0, 1000).trim()}`)

  if (summaryParts.length === 0) {
    return call1Content.slice(0, 3000)
  }

  return summaryParts.join('\n\n')
}

