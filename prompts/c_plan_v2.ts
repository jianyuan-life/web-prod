// User Prompt 構建（每個 Call 共用）
// ═══════════════════════════════════════════════════════════
export function buildUserPrompt(
  clientData: Record<string, any>,
  analyses: Array<Record<string, any>>,
  systemFilter: string[],
  birthData: Record<string, any>,
): string {
  const cd = clientData

  // ── 從西洋占星提取太陽/月亮/上升（防幻覺） ──
  const westernData = analyses.find((a: Record<string, any>) => a.system === '西洋占星')
  let sunSign = '', moonSign = '', ascSign = ''
  if (westernData?.detail) {
    const d = typeof westernData.detail === 'string' ? westernData.detail : JSON.stringify(westernData.detail)
    const sunMatch = d.match(/太陽[星座：]*\s*([^\s,，。]+(?:\s*[\d.]+°)?)/)
    const moonMatch = d.match(/月亮[星座：]*\s*([^\s,，。]+(?:\s*[\d.]+°)?)/)
    const ascMatch = d.match(/上升[星座：]*\s*([^\s,，。]+(?:\s*[\d.]+°)?)/)
    if (sunMatch) sunSign = sunMatch[1]
    if (moonMatch) moonSign = moonMatch[1]
    if (ascMatch) ascSign = ascMatch[1]
  }

  // ── 從數字能量提取生命靈數 ──
  const numerologyData = analyses.find((a: Record<string, any>) => a.system === '數字能量學')
  let lifePathNumber = ''
  if (numerologyData?.detail) {
    const nd = typeof numerologyData.detail === 'string' ? numerologyData.detail : JSON.stringify(numerologyData.detail)
    const m = nd.match(/生命靈數[：:]*\s*(\d+)/)
    if (m) lifePathNumber = m[1]
  }

  // ── 從八字找日主 ──
  const baziData = analyses.find((a: Record<string, any>) => a.system === '八字四柱' || a.system === '八字命理')
  let dayMaster = ''
  if (baziData?.sub_summary) {
    const m = baziData.sub_summary.match(/日[主柱][：:]*\s*([甲乙丙丁戊己庚辛壬癸])/)
    if (m) dayMaster = m[1]
  }
  if (!dayMaster && baziData?.detail) {
    const d = typeof baziData.detail === 'string' ? baziData.detail : JSON.stringify(baziData.detail)
    const m = d.match(/日主[為是：:]*\s*([甲乙丙丁戊己庚辛壬癸])/)
    if (m) dayMaster = m[1]
  }
  const persona = dayMaster ? getPersonaByDayMaster(dayMaster) : null

  // ── 組 user prompt ──
  let prompt = `═══════════════════════════════════════════\n`
  prompt += `【客戶基本資料】\n`
  prompt += `═══════════════════════════════════════════\n`
  prompt += `姓名：${cd.name || '未提供'}\n`
  const genderLabel = cd.gender === 'M' ? '男' : '女'
  prompt += `性別：${genderLabel} ⚠️ 這是${genderLabel}性客戶，全文稱呼不得弄反（${genderLabel === '男' ? '絕不可用「女人／姑娘／小姐」' : '絕不可用「男人／先生／兄」'}）\n`
  prompt += `出生：${cd.year}年${cd.month}月${cd.day}日${cd.hour !== undefined ? cd.hour + '時' : '時辰未知'}\n`
  prompt += `出生地：${cd.birth_city || birthData.birth_city || '未提供'}\n`
  if (cd.birth_country) prompt += `出生國家：${cd.birth_country}\n`
  if (cd.calendar_type) prompt += `曆法：${cd.calendar_type === 'lunar' ? '農曆' : '國曆'}\n`
  if (cd.age !== undefined) prompt += `當前年齡：${cd.age} 歲\n`
  prompt += `\n`

  prompt += `═══════════════════════════════════════════\n`
  prompt += `【關鍵鎖定（禁止幻覺）】\n`
  prompt += `═══════════════════════════════════════════\n`
  if (sunSign) prompt += `西洋太陽星座：${sunSign}\n`
  if (moonSign) prompt += `西洋月亮星座：${moonSign}\n`
  if (ascSign) prompt += `西洋上升星座：${ascSign}\n`
  if (lifePathNumber) prompt += `生命靈數：${lifePathNumber}\n`
  if (dayMaster) prompt += `八字日主：${dayMaster}${persona ? `（人格封號可用「${persona.title}」作為性格象徵）` : ''}\n`
  prompt += `當前年份：2026 年（丙午年）\n`
  prompt += `明年：2027 年（丁未年）\n`
  prompt += `\n`

  prompt += `═══════════════════════════════════════════\n`
  prompt += `【15 系統完整排盤數據】\n`
  prompt += `═══════════════════════════════════════════\n\n`

  for (const sys of systemFilter) {
    const a = analyses.find(item => item.system === sys)
    if (!a) continue
    prompt += `### ${sys}\n`
    if (a.sub_summary) prompt += `摘要：${a.sub_summary}\n`
    if (a.detail) {
      const detail = typeof a.detail === 'string' ? a.detail : JSON.stringify(a.detail, null, 2)
      prompt += `詳情：${detail.slice(0, 3000)}\n`
    }
    if (a.tables?.length) {
      for (const t of a.tables) {
        if (t.title) prompt += `\n【${t.title}】\n`
        if (t.headers) prompt += t.headers.join(' | ') + '\n'
        if (t.rows) {
          for (const row of t.rows) {
            prompt += row.join(' | ') + '\n'
          }
        }
      }
    }
    prompt += `\n`
  }

  return prompt
}

