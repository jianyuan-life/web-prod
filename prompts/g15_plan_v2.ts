// Feature Flag Helper:G15 v1 vs v2 切換
// (階段 ④ Round 2 在 plan-prompts.ts / steps.ts 接線時用)
// ============================================================
export function getG15PromptVersion(reportId?: string): 'v1' | 'v2' {
  const v2Enabled = process.env.G15_PROMPT_V2_ENABLED === 'true'
  const testReports = (process.env.G15_V2_TEST_REPORTS || '').split(',').map(s => s.trim()).filter(Boolean)
  if (v2Enabled || (reportId && testReports.includes(reportId))) {
    return 'v2'
  }
  return 'v1'
}
