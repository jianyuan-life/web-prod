// Helper:從 Call 1 抽摘要供 Call 2 / Call 3 接續
// ============================================================
export function extractG15Call1Summary(call1Content: string): string {
  // 抽 Hero 5 件套 + 各章節結尾三段式總結(🟢🟡🔵)
  const lines = call1Content.split('\n')
  const summary: string[] = []
  let inSummaryBlock = false
  for (const line of lines) {
    if (line.match(/^>\s*\*\*(主角|家庭結構|主要動力|主要業力|共同願景)/)) {
      summary.push(line)
    }
    if (line.match(/🟢|🟡|🔵/)) inSummaryBlock = true
    if (inSummaryBlock && line.trim()) summary.push(line)
    if (inSummaryBlock && line.match(/^---|^##/)) inSummaryBlock = false
  }
  return summary.slice(0, 60).join('\n').slice(0, 3500)  // 限制 3.5K 字以內、避免 Call 2 input 爆
}

export function extractG15Call1And2Summary(call1: string, call2: string): string {
  return extractG15Call1Summary(call1) + '\n\n---\n\n' + extractG15Call1Summary(call2)
}

