// ── user prompt 結構指引 ──
export function getDPlanStructurePrompt(): string {
  return `你必須嚴格按照以下結構輸出。直接從第一個章節標題開始。

【最重要的規則】整份報告必須 100% 圍繞客戶的問題展開。
客戶問語言發展，你就分析語言。客戶問該不該離職，你就分析離職。
不要寫「命格全面分析」——那是人生藍圖（C方案）的事。
D方案是「用命格回答問題」，不是「用問題引出命格分析」。

---

## 你的問題

把客戶的原始問題用引言框完整呈現，讓客戶知道「我的問題被認真看待了」：

> 「___（客戶的原始問題，一字不改地引用）」

---

## 你的答案

這是整份報告最重要的部分。客戶打開報告 10 秒內就要知道答案。

用一個醒目的引言框，**直接回答客戶的具體問題**：

> **答案：___**

注意：答案必須直接回應客戶問的那件事。
- 客戶問「孩子不會講話怎麼辦」→ 答案必須是「先做 X，因為 Y」，不是「這個孩子命格很好」
- 客戶問「該不該離職」→ 答案必須是「該」或「不該」
- 客戶問「成長要注意什麼」→ 答案必須列出具體的注意事項

然後用 3-5 句話解釋這個答案的核心邏輯。引用 2-3 個最關鍵的命理系統作為依據。

最後一段：「但這個答案有一個前提條件——___。如果你做不到這一點，結論會完全不同。」

---

## 一、深入解析——命格怎麼看這件事

> 一句話直戳問題本質：「你真正的問題不是___，而是___。」

這一章必須 100% 圍繞客戶的問題。不是分析整個命格，是用命格的相關面向來解答問題。
例如：客戶問語言發展 → 分析命格中跟「溝通、表達、學習」相關的系統
例如：客戶問事業 → 只分析跟「事業、財運、行動力」相關的系統

這不是復述客戶的問題。是用命理的視角，幫客戶看清楚他們「以為的問題」和「真正的問題」之間的落差。

### 命格與這個問題的關聯
從排盤數據中找到直接相關的命理線索。只用跟問題最相關的 5-8 套系統。
- **最核心的發現**：哪個系統/哪個組合直接指向問題的答案？
- **佐證**：還有哪些系統印證了這個結論？
- 每個發現用**粗體結論**開頭，再展開解釋

### 為什麼是現在？（流年/大運分析）
為什麼這個問題在這個時間點特別困擾你？
- 大運+流年如何觸發了這個議題
- 這個窗口期的性質：是危機還是轉機？

---

## 二、根源剖析——你為什麼會卡在這裡

> 一句當頭棒喝：「你一直在___，但真正需要面對的是___。」

深入分析這個問題為什麼會出現。三個層面，不用全寫，挑跟問題最相關的：

### 命格層面
你的命格結構中，哪些特質讓你容易在這個議題上卡住？
- 交叉引用多系統（標明系統名稱）
- 具體到行為模式：「你是不是常常___？這就是因為___。」

### 流年層面
大運流年如何加劇了這個問題？是短期波動還是長期趨勢？

### 心理層面（簡短）
用 2-3 句白話心理學解釋行為模式的根源（不用術語堆砌）。

---

## 三、你的路——怎麼走出來

> 一句話定調：「你的出路不是___，而是___。」

### 最佳行動方案
根據命格和流年，給出最適合你的行動路線。
- **短期（1-3個月）**：現在立刻要做的 2-3 件事（用 → 開頭，具體到明天就能做）
- **中期（3-6個月）**：接下來要布局的事
- **長期（6-12個月）**：最終目標和節奏

### 最佳時機
- 用表格呈現：什麼時候行動最有利、什麼時候按兵不動
- 交叉引用大運/流年/流月

### 最大風險
這條路上最容易踩的 2-3 個坑（附預防方案）

---

## 四、好的地方

列出 4-6 項對解決這個問題有利的命格優勢。

每項格式：
**{編號}. {一句話標題}**
- 命理依據（引用具體系統和數據）
- 怎麼善用（具體到行為層面）
- 最佳啟動時機

---

## 五、需要注意的地方

列出 4-6 項可能讓問題惡化的風險。一針見血，直接戳痛點。

每項格式：
**{編號}. {一句話標題}**
- 問題根源（引用具體系統和數據）
- 什麼時候最容易爆發
- → 出路：遇到這個狀況時具體該怎麼做

---

## 六、改善建議詳解

列出 5-7 項具體的行動處方箋。不是泛泛的建議，是根據你的命格量身定做的。

每項格式：
**{編號}. {行動名稱}**（用衝擊性的話命名，不是「建議一」）
- **為什麼是你**：命格中的哪個特質讓這個行動特別適合你
- **怎麼做**（3步，每步具體到明天就能做）：
  → 第一步：...
  → 第二步：...
  → 第三步：...
- **不做會怎樣**：直戳代價
- **做了會怎樣**：具體到看得見的改變
- **最佳時機**：什麼時候開始效果最好

---

## 寫給你的話

用 2-3 段話，像一封私人信件。

第一段：回應客戶的困惑——「我知道你問這個問題的時候心裡有多掙扎...」（讓客戶感到被理解）
第二段：給出力量——結合命格中最強的特質，告訴客戶「你比你以為的更有能力解決這個問題」
第三段：收尾行動——「從明天開始，做這一件事：___。」給一個最具體、最小的第一步。

字數要求：全文 8,000-15,000 字。以解決問題為標準，寫完整寫透。
核心原則：所有分析必須基於排盤數據，每個論點都要有數據支撐。先給答案，再給理由。`
}
